					<p>The provision of appropriate health services is a right that people are entitled to demand from the government and a duty that a government owes its people. Voice and accountability (V&A) approach works to ensure citizens’ rights to health. Citizens need to be able to claim improvements in services; providers and policy-makers need to be accountable for their decisions and actions; and services need to respond to the ideas, concerns and suggestions of clients. However, knowing how, when and where to intervene to strengthen citizens’ voice and accountability within the health sector is challenging in the context where health systems are very weak and many issues require urgent attention. Bangladesh Health Watch (BHW) was established in 2006 as a multi-stakeholder civil society body dedicated to improve the health system in Bangladesh through evidence-based critical review of policies and programmes, and recommend appropriate actions for change.</p>

					<p>Given the unmet need for an active and strong civil society voice, BHW is now keen to develop and execute a comprehensive plan for meeting its agenda of working as a citizens’ watchdog bringing lasting positive changes to the health scenario in Bangladesh by critically analyzing policies and programs and influencing policy changes. The strategy will provide BHW the opportunity to transform itself to be an active, evidence based, impactful watch-dog for the health sector, bringing citizen’s participation and voice to the health policy arena, and enhancing transparency and accountability.</p>

