					<h4>1. Monitoring progress of health related manifesto</h4>

					<p>Bangladesh Health Watch will commission annual reviews of progress against the manifesto and in its watchdog role, disseminate information on progress and carry out advocacy to increase effectiveness of commitments made, improve implementation speed and/or quality and suggest changes where required.  This review will be carried out every January, to mark the completion of an annual term of the government. The results along with recommendations from the civil society will be arrived at through an extended Thematic Group consisting of representatives from the Government policy planners, civil society, programme experts, NGO and private sector representatives, development partners and other relevant agencies. Findings and recommendations will be disseminated through media briefing followed by more detailed briefing and discussions with concerned stakeholder groups- e.g., Parliamentary Standing Committee, Minister/State Minister, and senior officials of MOHFW.</p>

					<h4>2. Review of health sector allocation in national budget </h4>

					<p>The allocation for health sector in successive national budgets is decreasing in terms of proportion, although the absolute amount of money has been increasing. The decreasing allocations hinder major investment in the sector which stands in the way of driving the sector forward. Every year, BHW will carry out extensive desk review of the health sector allocations in the preceding budgets to identify areas which are important for quality of care, accountability and equity and make strong recommendations for the budget planners on priority areas for investment.  During the analysis, attempts will be made to disaggregate data on spending on issues by gender; the recommendations to be placed to the government will also consider the specific needs of women and girls. The findings of the review will be shared with the press and electronic media to ensure that BHW’s voice is not lost in drawing up the budget. Public debates through media, op-eds in newspapers, submission of citizens’ memorandum to the government are some of the means which will be explored to generate energy around the demand for increasing the allocation and its effectiveness.</p>

					<h4>3. Strengthening BHW as a civil society platform</h4>

					<p>Under the proposed project, the Working Group of Bangladesh Health Watch will be expanded and its workings streamlined. The Working Group, along with external experts had undergone a process to revisit its goals, review its membership and devise ways to further strengthen its work. Subsequently, a strategy outline was developed on the basis of which process to induct new members, develop terms of reference for the Working Group and Advisory Group, develop strategic direction, and streamline meetings, have started. Under this project, the membership will be further expanded to make the Working Group even more representative of the civil society, draw more women into the Working Group and make the Working Group a wider coalition for civil society movement on health.  Representative(s) of donor(s) will be included in the meetings of the Working Group to attend as observers. The Advisory Committee will also be revived and reactivated, along the lines of the Working Group.</p>

