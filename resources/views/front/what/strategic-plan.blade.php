					<h4>1. Context</h4>

					<p>Bangladesh Health Watch (BHW) was established in 2006 as a civil society initiative to monitor Bangladesh’s progress towards achievement of the goal of good health for all its citizens. It was envisaged to provide the platform for citizens’ groups to take an active interest in understanding and sharing the challenges and choices, policy and programme options of the country’s health sector. </p>

					<p>Publication of the Bangladesh Health Watch Report has been a flagship activity of BHW. Providing in-depth analyses on topics of contemporary relevance to the health sector, BHW reports have played important role in bringing issues to the forefront through media coverage of the reports and the launching events attended by various luminaries and scholars, in addition to adding to the body of knowledge. So far six issues have been published and the seventh one is currently under development. In addition, in 2013 a series of health bulletins were also published.</p>
					<p>BHW’s activities are guided by a Working Group (WG), consisting of ten members. Major decisions are taken by the WG and implemented through a Secretariat, hosted by JPGSPH of BRACU. A senior team member of JPGSPH supports implementation of WG’s decision. More recently, a Coordinator has been appointed to lend more strategic support to gear up BHW. The WG is turn is supported by an Advisory Committee which was constituted at the time of inception and has met only once since then. With more than a decade having elapsed since its constitution, this Committee can be considered as defunct.</p>
					<p>BHW’s activities have so far been limited to the publication of its flagship reports. However, given the challenges facing Bangladesh’s health sector today and the almost complete absence of citizens’ voices in the policy arena, the need for a strong platform to raise issues and concerns, suggest solutions and  support and lobby the government in achieving its programmatic goals and international and national commitments is being strongly felt by various sectors of the society including academics, programme experts, development partners. In this context, the WG feels that there is need to re-invigorate BHW to play a wider and stronger role in influencing policies and bringing citizens’ voice to the forefront. Since the WG is the main driving force of BHW, developing a set of terms of reference for the WG to enable it to meet the expectations of its key stakeholder is therefore essential.</p>

					<h4>2. BHW’s Challenges</h4>

					<p>BHW has received sporadic funding from various development partners aimed at supporting the publication and launching of the BHW Reports. The first issue was supported by BRAC, the next two by SIDA and the later three by Rockefeller Foundation as part of a bigger grant to BRACU. The resources for carrying out day to day tasks- financial and administrative, communication support and day to day strategic support has been provided by JPGSPH pro-bono. While this level of support enabled BHW to carry on activities related to the publication of the Reports, it is recognised that this way of functioning cannot nurture and sustain a stronger  and more active BHW. Therefore a stronger Secretariat with the right skills mix and leadership capacity, a strong WG and a strong Advisory Committee to set the broad directions for BHW is needed. Alongside, a strategic plan to set BHW’s course for the next few years and resources to finance the activities of the strategic plan is also essential.</p>


